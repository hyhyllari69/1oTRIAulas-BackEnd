// BOOLEANOS: "true" e " false"
/*Operadores de comparação:
== -> Igualdade
!= -> Desigualdade 
> -> Maior que
< -> Menor que
>= -> Maior ou igual que
<= -> Menor ou igual que
*Operador de igualdade estrita*
=== -> Igualdade estrita
!== -> Desigualdade estrita
*/
console.log(10 == "10")
console.log(true == 1)
console.log(false == 0)

// Igualdade Estrita

console.log(10 === 1)
console.log(true === 1)
console.log(false === 0)